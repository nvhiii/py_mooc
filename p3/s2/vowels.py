user_str = input("Please type in a string: ")

while True:
    if "a" in user_str:
        print(f"a found")
    else:
        print(f"a not found")

    if "e" in user_str:
        print(f"e found")
    else:
        print(f"e not found")

    if "o" in user_str:
        print(f"o found")
    else:
        print(f"o not found")
    
    break